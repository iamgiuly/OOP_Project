    private void button1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button1MouseClicked
        // TODO add your handling code here:
        try{
        Personalizzazione p=new Personalizzazione();
        colore=jTextField1.getText();
        cappuccio=jCheckBox1.isSelected();
        cerniera=jCheckBox2.isSelected();
        tasche=jCheckBox3.isSelected();
        quantita=Integer.parseInt(jTextField6.getText());
         if(pers=jCheckBox1.isSelected()){
              tipostampa=jComboBox1.toString();
              inchiostro=jComboBox2.toString();
              pp=p.prezzoPersonalizzazioni(tipostampa,inchiostro);
            }   
        float pu=felpa.CalcoloPU(colore,cerniera,cappuccio,tasche);
        float pt=(pu+pp)*quantita;
        JOptionPane.showMessageDialog(null,"Il preventivo √®: "+pt+"");
        }catch(IOException ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_button1MouseClicked

